package control_statement;

public class Switch {

	public static void main(String[] args) {
	
		String month="oct";
		
		switch (month)
		{
		case "jan": System.out.println("This is first month of the year");
		break;
		
		case "feb": System.out.println("This is second month of the year");
		break;
		
		case "mar": System.out.println("This is third month of the year");
		break;
		
		case "apr": System.out.println("This is fourth month of the year");
		break;
		
		case "may": System.out.println("This is fifth month of the year");
		break;
		
		case "jun": System.out.println("This is sixth month of the year");
		break;
		
		case "jul": System.out.println("This is seventh month of the year");
		break;
		
		case "aug": System.out.println("This is eight month of the year");
		break;
		
		case "sep": System.out.println("This is ninth month of the year");
		break;
		
		case "oct": System.out.println("This is tenth month of the year");
		break;
		
		case "nov": System.out.println("This is eleventh month of the year");
		break;
		
		case "dec": System.out.println("This is twelth month of the year");
		break;
		
		default: System.out.println("Please enter valid month");
		}
		

	}

}
